<template>
  <el-dialog
    :title="!dataForm.id ? '新增' : '修改'"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()" label-width="80px">
    <el-form-item label="测量类型，如角度、力" prop="attributeValue">
      <el-input v-model="dataForm.attributeValue" placeholder="测量类型，如角度、力"></el-input>
    </el-form-item>
    <el-form-item label="测量单位，如°，N" prop="attributeUnit">
      <el-input v-model="dataForm.attributeUnit" placeholder="测量单位，如°，N"></el-input>
    </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">取消</el-button>
      <el-button type="primary" @click="dataFormSubmit()">确定</el-button>
    </span>
  </el-dialog>
</template>

<script>
  export default {
    data () {
      return {
        visible: false,
        dataForm: {
          id: 0,
          attributeValue: '',
          attributeUnit: ''
        },
        dataRule: {
          attributeValue: [
            { required: true, message: '测量类型，如角度、力不能为空', trigger: 'blur' }
          ],
          attributeUnit: [
            { required: true, message: '测量单位，如°，N不能为空', trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.visible = true
        this.$nextTick(() => {
          this.$refs['dataForm'].resetFields()
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/sen/sensorattribute/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.attributeValue = data.sensorAttribute.attributeValue
                this.dataForm.attributeUnit = data.sensorAttribute.attributeUnit
              }
            })
          }
        })
      },
      // 表单提交
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/sen/sensorattribute/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'id': this.dataForm.id || undefined,
                'attributeValue': this.dataForm.attributeValue,
                'attributeUnit': this.dataForm.attributeUnit
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: '操作成功',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
